
CREATE TABLE "audience" (
	"audName"	TEXT,
	"minAge"	INTEGER,
	"maxAge"	INTEGER,
	PRIMARY KEY("audName")
);

CREATE TABLE "city" (
	"cid"	INTEGER,
	"cityName"	TEXT,
	"country"	TEXT,
	PRIMARY KEY("cid")
);

CREATE TABLE "publishers" (
	"pid"	INTEGER,
	"pubName"	TEXT,
	"cid"	INTEGER,
	FOREIGN KEY("cid") REFERENCES "city"("cid"),
	PRIMARY KEY("pid")
);

CREATE TABLE "people" (
	"sin"	INTEGER,
	"first"	TEXT,
	"last"	TEXT,
	"age"	INTEGER,
	"cid"	INTEGER,
	PRIMARY KEY("sin"),
	FOREIGN KEY("cid") REFERENCES "city"("cid")
);

CREATE TABLE "authors" (
	"sin" INTEGER UNIQUE NOT NULL,
	"aid" INTEGER,
	PRIMARY KEY("aid"),
	FOREIGN KEY("sin") REFERENCES "people"("sin")
);
	

CREATE TABLE "books" (
	"bid"	INTEGER,
	"title"	TEXT,
	"aid"	INTEGER,
	"pid"	INTEGER,
	"genre"	TEXT,
	"audience"	TEXT,
	PRIMARY KEY("bid"),
	FOREIGN KEY("audience") REFERENCES "audience"("audName"),
	FOREIGN KEY("aid") REFERENCES "authors"("aid"),
	FOREIGN KEY("pid") REFERENCES "publishers"("pid")
);

CREATE TABLE "store" (
	"sid"	INTEGER,
	"storeName"	TEXT,
	"cid"	INTEGER,
	PRIMARY KEY("sid"),
	FOREIGN KEY("cid") REFERENCES "city"("cid")
);

CREATE TABLE "read" (
	"bid"	INTEGER,
	"sin"	INTEGER,
	PRIMARY KEY("bid","sin"),
	FOREIGN KEY("sin") REFERENCES "people"("sin"),
	FOREIGN KEY("bid") REFERENCES "books"("bid")
);

CREATE TABLE "sells" (
	"sid"	INTEGER,
	"pid"	INTEGER,
	FOREIGN KEY("sid") REFERENCES "store"("sid"),
	FOREIGN KEY("pid") REFERENCES "publishers"("pid"),
	PRIMARY KEY("sid","pid")
);


